package br.edu.ufba.softvis.visminerweb.beans;

public class Metrics {
	
	private int wmc;
	private int atfd;
	private float tcc;
	
	public int getWmc() {
		return wmc;
	}
	public void setWmc(int wmc) {
		this.wmc = wmc;
	}
	public int getAtfd() {
		return atfd;
	}
	public void setAtfd(int atfd) {
		this.atfd = atfd;
	}
	public float getTcc() {
		return tcc;
	}
	public void setTcc(float tcc) {
		this.tcc = tcc;
	}
		
}
